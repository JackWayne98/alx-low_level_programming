/**
 * _islower - prints the alphabet in lowercase 10x
 *
 * Description: evaluates a character if it's c then return 1, otherwise 0
 * @c: receives a character
 * Return: 1 if c is lowercase, otherwise 0
 */
int _islower(int c)
{
	if (c == 99)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
